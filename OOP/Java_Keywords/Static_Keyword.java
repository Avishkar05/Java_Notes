package Java_Keywords;
/*
	Static keyword is use to memory management purpose because when we use Static keyword 
	then no need to create object to call perticular method or variable
*/

class demo1
{
	static int a=10;
	
}
public class Static_Keyword {

	public static void main(String[] args) {
		demo1 ob = new demo1();
		System.out.println(ob.a);

	}

}
