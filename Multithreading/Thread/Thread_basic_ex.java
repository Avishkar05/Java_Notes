package Thread;

public class Thread_basic_ex {

	public static void main(String[] args) {
		
		System.out.println(Thread.currentThread().getName());
		
//		Thread : 		-> Predefined class.
//		currentThread : -> Use to find address of the current Thread. 
//		getName :		-> use to find name of current Thread.
		
		System.out.println("Hello, This is thread!!");

	}

}